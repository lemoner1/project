package vo.school;

public class School 
{
	private String edu_loc;
	private String edu_spot;
	private String ed_name;
	private String post_code;
	private String address;
	private String lat;
	private String lng;
	private String seq;
	
	
	public String getEdu_loc() {
		return edu_loc;
	}
	public void setEdu_loc(String edu_loc) {
		this.edu_loc = edu_loc;
	}
	public String getEdu_spot() {
		return edu_spot;
	}
	public void setEdu_spot(String edu_spot) {
		this.edu_spot = edu_spot;
	}
	public String getEd_name() {
		return ed_name;
	}
	public void setEd_name(String ed_name) {
		this.ed_name = ed_name;
	}
	public String getPost_code() {
		return post_code;
	}
	public void setPost_code(String post_code) {
		this.post_code = post_code;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLng() {
		return lng;
	}
	public void setLng(String lng) {
		this.lng = lng;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	
	

}
